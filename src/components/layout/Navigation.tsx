import Typography from "@/components/layout/Typography";
import URL, { NavItem } from "@/constants";
import { Menu, X } from "lucide-react";
import Image from "next/image";

interface NavigationProps {
  menuOpen: boolean;
  setMenuOpen: (open: boolean) => void;
  scrollY: number;
}

const Navigation: React.FC<NavigationProps> = ({
  menuOpen,
  setMenuOpen,
  scrollY,
}) => {
  return (
    <nav
      className="fixed top-0 w-full z-50 transition-all duration-300"
      style={{
        background: scrollY > 50 ? "rgba(199, 154, 119, 0.95)" : "transparent",
        backdropFilter: scrollY > 50 ? "blur(12px)" : "none",
      }}
    >
      <div className="max-w-9xl mx-auto px-4 sm:px-6 py-3 sm:py-4 flex items-center justify-between">
        <div className="flex items-center gap-2">
          <Image
            src={URL.LOGO}
            alt="Homestay Logo"
            width={44}
            height={44}
            priority
          />
          <Typography variant="brand" textColor="white" weight="bold" className="hover:text-black cursor-pointer">
            A Boutique Homestay
          </Typography>
        </div>

        <div className="hidden md:flex gap-4 lg:gap-8">
          {NavItem.map((item) => (
            <a
              key={item}
              href={`#${item.toLowerCase()}`}
              className="text-white hover:text-black transition-colors font-medium"
            >
              <Typography
                variant="paragraph"
                className="text-white hover:text-black"
                weight="medium"
              >
                {item}
              </Typography>
            </a>
          ))}
        </div>
        
        <button
          onClick={() => setMenuOpen(!menuOpen)}
          className="md:hidden"
        >
          {menuOpen ? (
            <X size={26} className="text-[#0D0A1F]" />
          ) : (
            <Menu size={26} className="text-[#0D0A1F]" />
          )}
        </button>
      </div>

      {menuOpen && (
        <div className="md:hidden bg-[#C9A177] border-t border-[#BFC7DE]">
          <div className="flex flex-col gap-4 p-4 sm:p-6">
            {NavItem.map((item) => (
              <a
                key={item}
                href={`#${item.toLowerCase()}`}
                onClick={() => setMenuOpen(false)}
              >
                <Typography
                  variant="paragraph"
                  
                  weight="medium"
                  className="hover:text-black transition-colors text-white"
                >
                  {item}
                </Typography>
              </a>
            ))}
          </div>
        </div>
      )}
    </nav>
  );
};

export default Navigation;

